//Operadores de comparação

let num1 = 10;
let num2 = "10";
console.log(num1==num2); //true
console.log(typeof num1, typeof num2);  //number sytring
console.log(num1===num2);  //false
console.log(num1<=num2);  //true
console.log(num1>num2);  //false
console.log(num1!=num2); //false
console.log(num1!==num2); //true
